/**
 * The AnimationState enum represents the various states of bird animation.
 */
public enum AnimationState {
    /**
     * The bird is in the normal flying state.
     */
    FLYING,
    
    /**
     * The bird has been shot and is in the shot state.
     */
    SHOT,
    
    /**
     * The bird is falling after being shot.
     */
    FALLING
}